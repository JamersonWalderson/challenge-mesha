     * @param  \Illuminate\Http\Request  $request
     * @param  int  $id
     * @return \Illuminate\Http\Response
     */
    public function update(Request $request)
    {
        $doctor = Doctor::find($request->id);
        $doctor->name = $request->input('name');
        
        if($doctor->save()){
            return new DoctorCollection($doctor);
            
        }
    }

